// Helper functions for product operations
export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category);
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.isFeatured);
}

export function getNewProducts(): Product[] {
  return products.filter(p => p.isNew);
}

export function getSaleProducts(): Product[] {
  return products.filter(p => p.isSale);
}

export function getRelatedProducts(productId: string, limit: number = 4): Product[] {
  const product = getProductById(productId);
  if (!product) return [];
  
  // Get products from same category (same type)
  const sameCategory = products
    .filter(p => p.category === product.category && p.id !== productId)
    .slice(0, Math.ceil(limit / 2));
  
  // Get completely different products (for variety/serendipity)
  const differentCategory = products
    .filter(p => p.category !== product.category && p.id !== productId)
    .sort(() => Math.random() - 0.5) // Shuffle for variety
    .slice(0, Math.floor(limit / 2));
  
  // Shuffle and combine
  const related = [...sameCategory, ...differentCategory];
  return related.sort(() => Math.random() - 0.5).slice(0, limit);
}

export function getRecommendedProducts(productId: string, limit: number = 8): Product[] {
  const product = getProductById(productId);
  if (!product) return [];
  
  // Same category products (same type) - 60% of recommendations
  const sameCategory = products
    .filter(p => p.category === product.category && p.id !== productId)
    .sort((a, b) => b.rating - a.rating); // Sort by rating
  
  // Different category products (filtered variety) - 40% of recommendations
  const differentCategory = products
    .filter(p => p.category !== product.category && p.id !== productId)
    .sort((a, b) => b.reviewCount - a.reviewCount); // Sort by popularity
  
  const sameCategoryCount = Math.ceil(limit * 0.6);
  const differentCategoryCount = Math.floor(limit * 0.4);
  
  return [
    ...sameCategory.slice(0, sameCategoryCount),
    ...differentCategory.slice(0, differentCategoryCount)
  ].slice(0, limit);
}

export function searchProducts(query: string): Product[] {
  const lowercaseQuery = query.toLowerCase();
  return products.filter(p => 
    p.name.toLowerCase().includes(lowercaseQuery) ||
    p.description.toLowerCase().includes(lowercaseQuery) ||
    p.category.toLowerCase().includes(lowercaseQuery)
  );
}

export function filterProducts(filters: {
  category?: string;
  minPrice?: number;
  maxPrice?: number;
  sizes?: string[];
  colors?: string[];
  isNew?: boolean;
  isSale?: boolean;
}): Product[] {
  return products.filter(p => {
    if (filters.category && p.category !== filters.category) return false;
    if (filters.minPrice && p.price < filters.minPrice) return false;
    if (filters.maxPrice && p.price > filters.maxPrice) return false;
    if (filters.sizes && filters.sizes.length > 0 && !filters.sizes.some(s => p.sizes.includes(s))) return false;
    if (filters.colors && filters.colors.length > 0 && !filters.colors.some(c => p.colors.some(pc => filters.colors!.includes(pc.name)))) return false;
    if (filters.isNew && !p.isNew) return false;
    if (filters.isSale && !p.isSale) return false;
    return true;
  });
}

